// variables = allocated space in memory to store a value
#include<stdio.h>
int main()
{
    int x; //declaration 
    x=10; //initialization
    int y=90; //declaration + initialazation
    int age =19; // integer
    float gpa=8.8;//float 
    char grade='A';//character
    char name[]="Rohit";//array
    printf("Hello %s\n",name);
    printf("You are %d years old\n",age);
    printf("Your gpa is %f\n",gpa);
    printf("Your grade is %c\n",grade);
    return 0;
    // here x,y,age,gpa,grade all are variables
}